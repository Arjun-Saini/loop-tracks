/**
 * Converts a string of hexadecimal digits to an integer.
 * @param s The string of hexadecimal digits.
 * @return The integer.
 */
long x2i(char *);

/**
 * Converts a string of hexadecimal digits to an integer.
 * @param s The string of hexadecimal digits.
 * @return The integer.
 */
long x2i(const char *);

/**
 * Converts a string of hexadecimal digits to an unsigned integer.
 * @param s The string of hexadecimal digits.
 * @return The integer.
 */
unsigned long x2u(char *);

/**
 * Converts a string of hexadecimal digits to an unsigned integer.
 * @param s The string of hexadecimal digits.
 * @return The integer.
 */
unsigned long x2u(const char *);
